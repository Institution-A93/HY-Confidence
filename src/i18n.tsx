export const LangContext = createContext<LangCode>("en");

export function translate(lang: LangCode, key: string, vars?: Record<string, string | number>): string {
  const dict = I18N[lang] || I18N.en;
  let s = dict[key];
  if (s == null) s = I18N.en[key];
  if (s == null) s = key;
  if (vars) for (const k in vars) s = s.split("{" + k + "}").join(String(vars[k]));
  return s;
}

export type TFn = (key: string, vars?: Record<string, string | number>) => string;

export function useT(): TFn {
  const lang = useContext(LangContext);
  return (key, vars) => translate(lang, key, vars);
}

// Render an I18nPiece[] (server-generated localized content) → one string, translating + concatenating
// each piece. Returns null when there are no pieces, so callers fall back to the English `note`/`text`.
export function joinPieces(t: TFn, pieces?: { key: string; params?: Record<string, string | number> }[]): string | null {
  if (!pieces || pieces.length === 0) return null;
  return pieces.map((p) => t(p.key, p.params)).join("");
}
